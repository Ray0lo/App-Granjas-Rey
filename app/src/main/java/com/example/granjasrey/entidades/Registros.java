package com.example.granjasrey.entidades;

public class Registros {

    private int id;
    private String nombre;
    private String fecha;
    private String hora;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String telefono) {
        this.fecha = telefono;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String correo_electornico) {
        this.hora = correo_electornico;
    }
}
